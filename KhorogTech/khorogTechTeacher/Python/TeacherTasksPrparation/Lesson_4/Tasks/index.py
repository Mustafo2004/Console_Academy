
# ### Задача 1
# Напишите программу, которая проверяет, является ли введенное число четным или нечетным. Если число четное, выведите "Четное число", иначе выведите "Нечетное число".

# ### Задача 2
# Напишите программу, которая проверяет, какой день недели сегодня. Если это понедельник, выведите "Начало недели", если пятница, выведите "Почти выходные", а если выходные (суббота или воскресенье), выведите "Выходные".

# ### Задача 3
# Напишите программу, которая проверяет, попадает ли введенный возраст человека в диапазон от 18 до 65 лет (включительно). Если да, выведите "Работоспособный возраст", иначе выведите "Не работоспособный возраст".

# ### Задача 4
# Напишите программу, которая запрашивает у пользователя количество баллов за экзамен. Если баллы больше или равны 60, выведите "Сдал", иначе выведите "Не сдал".

# ### Задача 5
# Напишите программу, которая проверяет, входит ли введенное число в диапазон от 10 до 50 (включительно). Если да, выведите "В диапазоне", иначе выведите "Вне диапазона".

# ### Задача 6
# Напишите программу, которая проверяет, является ли введенное число положительным или отрицательным. Если число положительное, выведите "Положительное число", если отрицательное, выведите "Отрицательное число", и если число равно нулю, выведите "Ноль".

# ### Задача 7
# Напишите программу, которая проверяет, является ли введенное число кратным 3 или 5. Если кратно 3, выведите "Кратно 3", если кратно 5, выведите "Кратно 5", и если кратно и 3, и 5, выведите "Кратно 3 и 5".

# ### Задача 8
# Напишите программу, которая проверяет, является ли введенное число меньше 100. Если да, выведите "Меньше 100", иначе выведите "Больше или равно 100".

# ### Задача 9
# Напишите программу, которая запрашивает у пользователя время в часах (0-23) и выводит, является ли это время "Утро" (0-11), "День" (12-17) или "Вечер" (18-23).

# ### Задача 10
# Напишите программу, которая проверяет, является ли введенный возраст человека младше 13 лет. Если да, выведите "Детский возраст", иначе выведите "Не детский возраст".
