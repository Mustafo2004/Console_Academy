
# 1. **Task 1**: Введите название дня недели. Добавьте его в список, если его там нет. Если есть, выведите "День уже в списке".

# 2. **Task 2**: Введите количество студентов и их имена. Сохраните их в словарь с ключом как номер студента. Если имя уже есть в словаре, выведите "Имя уже существует".

# 3. **Task 3**: Введите число. Если число больше 50, добавьте его в список. Если число меньше 10, удалите его из списка, если оно там есть.

# 4. **Task 4**: Введите название фрукта. Если фрукт уже есть в списке, удалите его, иначе добавьте.

# 5. **Task 5**: Введите название страны и ее столицу. Если страна уже есть в словаре, обновите столицу, иначе добавьте новую запись.

# 6. **Task 6**: Введите число. Если число больше 100, выведите "Большое число". Если меньше 50, добавьте его в список.

# 7. **Task 7**: Введите имя и возраст человека. Если возраст больше 18, добавьте запись в словарь с именем и возрастом. Если меньше, выведите "Несовершеннолетний".

# 8. **Task 8**: Введите название книги и ее автора. Если книга уже есть в словаре, выведите "Книга уже существует". Если нет, добавьте запись в словарь.

# 9. **Task 9**: Введите температуру и время года. Если время года - лето, и температура выше 25, выведите "Тепло". Если зима и температура ниже 0, выведите "Холодно".

# 10. **Task 10**: Введите имя сотрудника и его зарплату. Если зарплата больше 50000, добавьте запись в словарь. Если меньше, выведите "Низкая зарплата".

# 11. **Task 11**: Введите число. Если оно делится на 2, добавьте его в список четных чисел. Если на 3, добавьте в список чисел, делящихся на 3.

# 12. **Task 12**: Введите слово. Если слово начинается с гласной, добавьте его в список гласных слов. Если с согласной, добавьте в список согласных слов.

# 13. **Task 13**: Введите имя студента и его оценку. Если оценка больше 90, добавьте запись в словарь отличников. Если меньше 60, добавьте в словарь двоечников.

# 14. **Task 14**: Введите название города и его население. Если население больше миллиона, добавьте запись в словарь мегаполисов. Если меньше 100000, добавьте в словарь малых городов.

# 15. **Task 15**: Введите цвет. Если цвет - красный, синий или зеленый, добавьте его в список основных цветов. Если нет, добавьте в список дополнительных цветов.

# 16. **Task 16**: Введите название страны и ее население. Если население больше 50 миллионов, добавьте запись в словарь крупных стран. Если меньше 10 миллионов, добавьте в словарь малых стран.

# 17. **Task 17**: Введите имя и возраст. Если возраст больше 18 и имя начинается с буквы "А", добавьте запись в словарь взрослых с именами на "А". Если меньше 18, добавьте в словарь детей.

# 18. **Task 18**: Введите день недели. Если это выходной, добавьте в список выходных дней. Если рабочий день, добавьте в список рабочих дней.

# 19. **Task 19**: Введите имя и оценку студента. Если оценка больше 80, добавьте запись в список хороших студентов. Если меньше 50, добавьте в список плохих студентов.

# 20. **Task 20**: Введите название животного. Если это домашнее животное, добавьте его в список домашних животных. Если дикое, добавьте в список диких животных.

