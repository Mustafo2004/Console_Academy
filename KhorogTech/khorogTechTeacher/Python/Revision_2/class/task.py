
# 1. **Task 1**: Создайте словарь с ключами - именами студентов и значениями - их возрастом. Если возраст больше 18, выведите сообщение "Совершеннолетний", иначе "Несовершеннолетний".
   
# 2. **Task 2**: Введите значение ключа для словаря, если ключ существует, выведите его значение, иначе выведите "Ключ не найден".
   
# 3. **Task 3**: Создайте список чисел. Если число четное, добавьте его в новый список, если нет, пропустите его.
   
# 4. **Task 4**: Введите название фрукта. Если фрукт в списке ["яблоко", "банан", "вишня"], выведите "Фрукт найден", иначе "Фрукт не найден".
   
# 5. **Task 5**: Создайте словарь с ключами - названиями предметов и значениями - их цены. Если цена больше 100, выведите "Дорогой предмет", иначе "Дешевый предмет".
   
# 6. **Task 6**: Введите имя студента. Если имя есть в словаре, удалите его. Если его нет, выведите "Студент не найден".
   
# 7. **Task 7**: Введите число. Если оно больше 10, добавьте его в список. Если меньше 5, выведите "Слишком мало".
   
# 8. **Task 8**: Создайте список строк. Если длина строки больше 5 символов, замените ее на "Длинная строка", иначе оставьте без изменений.
   
# 9. **Task 9**: Введите оценку студента. Если оценка больше 75, выведите "Отлично", если от 50 до 75, выведите "Хорошо", иначе "Плохо".
   
# 10. **Task 10**: Создайте словарь с именами студентов и их оценками. Если оценка больше 80, выведите имя и "Отличник".
   
# 11. **Task 11**: Введите день недели. Если день в списке ["понедельник", "вторник", "среда"], выведите "Рабочий день", если "суббота" или "воскресенье", выведите "Выходной".
   
# 12. **Task 12**: Создайте список чисел. Если число больше 0, добавьте его в другой список, если меньше 0, выведите "Отрицательное число".
   
# 13. **Task 13**: Введите название цвета. Если цвет в списке ["красный", "зеленый", "синий"], выведите "Основной цвет", иначе "Дополнительный цвет".
   
# 14. **Task 14**: Создайте словарь с ключами - названиями городов и значениями - числом жителей. Если число больше 1 миллиона, выведите "Мегаполис", иначе "Город".
   
# 15. **Task 15**: Введите имя сотрудника. Если имя в списке, удалите его, если нет, добавьте его.
   
# 16. **Task 16**: Создайте список чисел. Если число делится на 3, замените его на "Fizz", если на 5, замените на "Buzz", если на оба, замените на "FizzBuzz".
   
# 17. **Task 17**: Введите температуру. Если температура выше 30, выведите "Жарко", если от 10 до 30, выведите "Тепло", иначе "Холодно".
   
# 18. **Task 18**: Создайте словарь с названиями месяцев и количеством дней. Если месяц имеет 31 день, выведите "Длинный месяц", иначе "Короткий месяц".
   
# 19. **Task 19**: Введите число. Если число четное, выведите "Четное", если нет, выведите "Нечетное".
   
# 20. **Task 20**: Создайте список строк. Если строка начинается с буквы "А", добавьте ее в новый список, иначе пропустите ее.
