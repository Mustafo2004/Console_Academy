
# 1. **Task 1**: Введите возраст. Если возраст меньше 18, выведите "Несовершеннолетний", если от 18 до 65, выведите "Взрослый", иначе "Пожилой".

# 2. **Task 2**: Введите число. Если число больше 100, выведите "Большое число", если равно 100, выведите "Равно 100", иначе "Маленькое число".

# 3. **Task 3**: Введите имя. Если имя начинается с буквы "А", выведите "Имя начинается с А", иначе "Имя не начинается с А".

# 4. **Task 4**: Введите температуру в градусах Цельсия. Если температура ниже 0, выведите "Мороз", если от 0 до 20, выведите "Прохладно", иначе "Тепло".

# 5. **Task 5**: Введите число. Если число четное, выведите "Четное число", иначе "Нечетное число".

# 6. **Task 6**: Введите название месяца. Если месяц - летний (июнь, июль, август), выведите "Летний месяц", если зимний (декабрь, январь, февраль), выведите "Зимний месяц", иначе "Не летний и не зимний месяц".

# 7. **Task 7**: Введите оценку от 1 до 5. Если оценка 5, выведите "Отлично", если 4, выведите "Хорошо", если 3, выведите "Удовлетворительно", иначе "Неудовлетворительно".

# 8. **Task 8**: Введите день недели. Если день - выходной (суббота, воскресенье), выведите "Выходной день", иначе "Рабочий день".

# 9. **Task 9**: Введите число. Если число положительное, выведите "Положительное число", если отрицательное, выведите "Отрицательное число", иначе "Число равно нулю".

# 10. **Task 10**: Введите длину строки. Если длина строки больше 10, выведите "Длинная строка", если меньше 5, выведите "Короткая строка", иначе "Строка средней длины".

# 11. **Task 11**: Введите текущий час (от 0 до 23). Если час от 6 до 11, выведите "Утро", если от 12 до 17, выведите "День", если от 18 до 23, выведите "Вечер", иначе "Ночь".

# 12. **Task 12**: Введите сумму покупки. Если сумма больше 1000, выведите "Скидка 10%", если больше 500, выведите "Скидка 5%", иначе "Скидка не предоставляется".

# 13. **Task 13**: Введите пароль. Если пароль содержит менее 8 символов, выведите "Пароль слишком короткий", если содержит только буквы, выведите "Пароль должен содержать цифры", иначе "Пароль принят".

# 14. **Task 14**: Введите возраст. Если возраст меньше 7, выведите "Дошкольник", если от 7 до 17, выведите "Школьник", если от 18 до 22, выведите "Студент", иначе "Взрослый".

# 15. **Task 15**: Введите число. Если число делится на 3, выведите "Делится на 3", если делится на 5, выведите "Делится на 5", если делится на оба, выведите "Делится на 3 и 5", иначе "Не делится ни на 3, ни на 5".

# 16. **Task 16**: Введите баллы ЕГЭ. Если баллы больше 90, выведите "Отличный результат", если от 70 до 90, выведите "Хороший результат", если от 50 до 70, выведите "Удовлетворительный результат", иначе "Низкий результат".

# 17. **Task 17**: Введите скорость автомобиля. Если скорость меньше 60, выведите "Низкая скорость", если от 60 до 120, выведите "Средняя скорость", если больше 120, выведите "Высокая скорость".

# 18. **Task 18**: Введите количество сотрудников. Если количество меньше 10, выведите "Маленькая компания", если от 10 до 100, выведите "Средняя компания", иначе "Большая компания".

# 19. **Task 19**: Введите длину отрезка. Если длина меньше 1, выведите "Маленький отрезок", если от 1 до 5, выведите "Средний отрезок", иначе "Большой отрезок".

# 20. **Task 20**: Введите количество страниц книги. Если страниц меньше 100, выведите "Короткая книга", если от 100 до 300, выведите "Средняя книга", иначе "Длинная книга".
