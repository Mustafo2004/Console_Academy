# Создай словарь student_grades для хранения оценок студентов. Запроси у пользователя ввести имя студента и его оценку, и добавь эту информацию в словарь. Повтори это три раза. Затем запроси имя студента и выведи его оценку. Если такого студента нет в словаре, выведи сообщение об этом.
student_grades = {}

# Добавление оценок для трех студентов
for _ in range(3):
    name = input("Введите имя студента: ")
    grade = input("Введите оценку студента: ")
    student_grades[name] = grade

# Запрос имени студента для поиска его оценки
search_name = input("Введите имя студента для поиска его оценки: ")
if search_name in student_grades:
    print(f"Оценка студента {search_name}: {student_grades[search_name]}")
else:
    print(f"Студент с именем {search_name} не найден.")


# Создай словарь capital_cities с названиями стран и их столицами: "Россия": "Москва", "Франция": "Париж", "Япония": "Токио". Запроси у пользователя ввести страну и выведи столицу этой страны. Если страна не найдена, выведи сообщение об этом.
capital_cities = {
    "Россия": "Москва",
    "Франция": "Париж",
    "Япония": "Токио"
}

country = input("Введите название страны: ")
if country in capital_cities:
    print(f"Столица {country}: {capital_cities[country]}")
else:
    print(f"Страна {country} не найдена в словаре.")

# Создай словарь phone_book для хранения номеров телефонов. Запроси у пользователя ввести имя и номер телефона, и добавь эту информацию в словарь. Повтори это два раза. Затем выведи весь телефонный справочник.
phone_book = {}

for _ in range(2):
    name = input("Введите имя: ")
    phone = input("Введите номер телефона: ")
    phone_book[name] = phone

print("Телефонный справочник:")
for name, phone in phone_book.items():
    print(f"{name}: {phone}")


# Создай словарь inventory для хранения количества товаров. Добавь в словарь три товара: "яблоко" с количеством 10, "банан" с количеством 5 и "апельсин" с количеством 7. Запроси у пользователя ввести название товара и количество, и обнови информацию в словаре. Затем выведи обновленный словарь.
inventory = {
    "яблоко": 10,
    "банан": 5,
    "апельсин": 7
}

item = input("Введите название товара: ")
quantity = int(input("Введите количество товара: "))
inventory[item] = quantity

print("Обновленный инвентарь:")
for item, quantity in inventory.items():
    print(f"{item}: {quantity}")

# Создай словарь book_info для хранения информации о книге. Запроси у пользователя ввести название книги, автора и год издания, и добавь эту информацию в словарь. Затем выведи информацию о книге.
book_info = {}

title = input("Введите название книги: ")
author = input("Введите автора книги: ")
year = input("Введите год издания книги: ")

book_info["Название"] = title
book_info["Автор"] = author
book_info["Год издания"] = year

print("Информация о книге:")
for key, value in book_info.items():
    print(f"{key}: {value}")
