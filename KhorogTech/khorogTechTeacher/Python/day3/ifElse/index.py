# Напишите программу, которая принимает температуру в градусах Цельсия и выводит, холодно на улице, тепло или жарко.
temperature = int(input("Введите температуру в градусах Цельсия: "))
if temperature < 10:
    print("Холодно")
elif 10 <= temperature <= 25:
    print("Тепло")
else:
    print("Жарко")
