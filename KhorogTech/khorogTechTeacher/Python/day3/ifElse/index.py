# Напишите программу, которая принимает количество осадков (в мм) и выводит, идёт ли дождь, снег или солнечная погода.
precipitation = int(input("Введите количество осадков (в мм): "))
if precipitation > 0 and precipitation <= 10:
    print("Идёт небольшой дождь")
elif precipitation > 10:
    print("Идёт сильный дождь")
elif precipitation < 0:
    print("Идёт снег")
else:
    print("Солнечная погода")

# Напишите программу, которая принимает месяц (число от 1 до 12) и выводит время года (зима, весна, лето, осень).
month = int(input("Введите номер месяца (от 1 до 12): "))
if month in [12, 1, 2]:
    print("Зима")
elif month in [3, 4, 5]:
    print("Весна")
elif month in [6, 7, 8]:
    print("Лето")
elif month in [9, 10, 11]:
    print("Осень")
else:
    print("Некорректный номер месяца")
# Напишите программу, которая принимает скорость интернета (в Мбит/с) и выводит, медленный, средний или быстрый интернет.
speed = int(input("Введите скорость интернета (в Мбит/с): "))
