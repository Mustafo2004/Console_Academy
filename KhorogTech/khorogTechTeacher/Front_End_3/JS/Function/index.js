// Создайте функцию, которая принимает строку и возвращает её в верхнем регистре.
function toUpperCase(str) {
    return str.toUpperCase();
}

// Создайте стрелочную функцию, которая принимает массив чисел и возвращает новый массив, состоящий только из чётных чисел.
const filterEvenNumbers = (arr) => {
    return arr.filter(num => num % 2 === 0);
}

// Создайте функцию, которая принимает два массива чисел и возвращает новый массив, содержащий элементы обоих массивов.
function mergeArrays(arr1, arr2) {
    return arr1.concat(arr2);
}

